%------------------------------------------------
%Program to plot a sphere.
%Author: Abhinav Roy
%------------------------------------------------
theta = linspace(0, 2*pi, 200);
phi = linspace(-pi/2, pi/2, 200);
R = 2;
%------------------------------------------------
[u,v] = meshgrid(phi,theta);
x = R.*cos(u).*cos(v);
y = R.*cos(u).*sin(v);
z = R.*sin(u);
%------------------------------------------------
plot3(x,y,z);
axis('equal');
grid ON;
%------------------------------------------------